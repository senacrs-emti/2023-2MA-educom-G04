                    <ol><b>1.Nucléolo:</b>é uma região que pode ser vista no interior do núcleo e possui formato arredondado. O nucléolo é formado, principalmente, por RNA ribossomal e proteínas. Esse RNA é sintetizado a partir de instruções do DNA. </ol>
                    <ol><b>2.Núcleo celular:</b>é a região da célula onde ocorre o controle de todas as atividades celulares. </ol>
                    <ol><b>3.Ribossomos:</b> são estruturas celulares responsáveis pela síntese de proteínas. Essas estruturas são formadas por duas subunidades, um maior e uma menor, constituídas por moléculas de RNA (ácido ribonucleico) e proteínas.  </ol>
                    <ol><b>4.Vesículas:</b>transportam ou digerem produtos celulares ou resíduos. </ol>
                    <ol><b>5.Ergastoplasma ou Retículo endoplasmático rugoso:</b>retículo endoplasmático ao qual estão associados os ribossomos; </ol>
                    <ol><b>6.Complexo de Golgi:</b>é uma organela relacionada com a produção, processamento, empacotamento e endereçamento de uma série de substâncias no interior da célula. </ol>
                    <ol><b>7.Microtúbulos:</b>manutenção da célula, formação do fuso mitótico, formação e movimentação de cílios e flagelos e o movimento das organelas celulares e dos cromossomos durante o processo de mitose. </ol>
                    <ol><b>8.Retículo endoplasmático liso:</b>a sua principal função é a de transporte de substâncias dentro da célula, que funciona como uma rede de distribuição de substâncias que trabalham em seu interior.   </ol>
                    <ol><b>9. Mitocôndrias:</b>responsável pela respiração celular.</ol>
                    <ol><b>10.Vacúolo:</b>garante a digestão intracelular, ou seja, permite que a célula consiga digerir os produtos que captura. </ol>
                    <ol><b>11.Citoplasma:</b>abriga as organelas membranosas e o núcleo celular. Ele é responsável pelo armazenamento de substâncias químicas fundamentais para a manutenção da vida, além de auxiliar no processo de respiração celular.  </ol>
                    <ol><b>12.Lisossomos:</b>atua na digestão intracelular, garantindo a degradação de materiais capturados pela célula, bem como a reciclagem de alguns componentes celulares  </ol>
                    <ol><b>13.Centríolos:</b>são estruturas celulares que auxiliam na divisão celular (mitose e meiose).   </ol>

