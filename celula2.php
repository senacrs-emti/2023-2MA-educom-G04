        <p>A <b>célula vegetal</b> é o componente básico de todos os seres vivos que fazem parte do Reino Vegetal. Em conjunto, as células formam os tecidos que, consequente, formam os órgãos, os quais são responsáveis pela execução de funções vitais como respiração, alimentação e reprodução.   </p>
        <p>A célula dos vegetais é eucariótica, ou seja, possui núcleo organizado e vários tipos de organelas. Esses dois tipos de células possuem membrana plasmática, citoplasma e núcleo.   </p>
        <p>Contudo, parede celular, plastos e vacúolos são estruturas que pertencem apenas à célula vegetal, pois são específicas para o estilo de vida das plantas. </p>
