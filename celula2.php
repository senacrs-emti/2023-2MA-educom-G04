                <p class="p-lista"><b>B.Leucoplastos:</b>não possuem coloração e são incolores. Sua função é essencialmente armazenar substâncias como proteínas, amidos e lipídios, podendo ser nomeados de oleoplastos, proteoplastos, amiloplastos, etc. </p>
                    
